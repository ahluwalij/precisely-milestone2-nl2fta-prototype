  echo -n "Do you want to proceed? (y/N): "
  read -r CONFIRM
  if [[ ! "$CONFIRM" =~ ^[Yy]$ ]]; then
      echo -e "${YELLOW}Test cancelled by user${NC}"
      exit 0
  fi
