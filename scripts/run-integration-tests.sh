    echo ""
    
    echo -n "AWS User Access Key ID: "
    read -r ACCESS_KEY_ID
    
    echo -n "AWS User Secret Access Key: "
    read -rs SECRET_ACCESS_KEY
    echo ""
    
    echo -n "AWS Region [us-east-1]: "
